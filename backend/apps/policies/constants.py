# 지역코드 자체(5자리 숫자, 예: "11000")로 들어오는 경우와 구분하기 위한 패턴
REGION_CODE_PATTERN = r"^\d{5}$"


def resolve_region_code(region: str) -> str:
    """
    프론트에서 넘어온 region 파라미터를 지역코드로 변환한다.
    - 이미 지역코드 형태("11000")면 그대로 반환
    - 지역명("서울", "서울특별시" 등)이면 매핑 테이블에서 찾아 반환
    - 매칭되는 게 없으면 원본 값을 그대로 반환 (기존 동작 유지, 필터 결과가 0건이 될 수 있음)
    """
    import re

    region = (region or "").strip()
    if not region:
        return region

    if re.match(REGION_CODE_PATTERN, region):
        return region

    # 긴 이름부터 매칭해서 "전북"이 "전북특별자치도"보다 먼저 잘못 매칭되는 것을 방지
    for name, code in sorted(REGION_NAME_TO_CODE.items(), key=lambda x: len(x[0]), reverse=True):
        if name in region:
            return code

    return region